//usage of throw clause
class  ThrowTest3
{
	public static void main(String[] args) 
	{
		try
		{
			sample();
		}
		catch(NullPointerException e)
		{
			System.out.println("This is main Iam Catching exception:"+e);
		}
	}
	static void sample() throws NullPointerException
	{
		System.out.println("I am Sample Throwing a Null Pointer Exception");
		throw new NullPointerException();
	}
}
